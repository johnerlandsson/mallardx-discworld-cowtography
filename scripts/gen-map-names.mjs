// Generates src/data/map_names.lua from ui/data/rooms.js.
// Run: node scripts/gen-map-names.mjs

import { createWriteStream } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { rooms, maps } from '../ui/data/rooms.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '..', 'src', 'data', 'map_names.lua')

function luaStr(s) {
  return "'" + String(s ?? '')
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\n/g, '\\n')
    .replace(/\r/g, '')
    .replace(/\0/g, '') + "'"
}

const stream = createWriteStream(OUT)
stream.write('-- Auto-generated by scripts/gen-map-names.mjs. Do not edit by hand.\n')
stream.write('return {\n')
for (const [roomId, row] of Object.entries(rooms)) {
  const mapId = row[0]
  const name = maps[mapId]?.name ?? 'Unknown'
  stream.write(`  [${luaStr(roomId)}] = ${luaStr(name)},\n`)
}
stream.write('}\n')
stream.end(() => console.log(`Written: ${OUT}`))
